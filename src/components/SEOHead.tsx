import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: string;
  structuredData?: object;
}

const SEOHead: React.FC<SEOHeadProps> = ({
  title = "Active Soft - Oracle ERP Implementation & Custom Desktop Solutions | Egypt's Leading Software Company",
  description = "Transform your business with Active Soft's Oracle ERP implementation, custom Windows desktop applications, and enterprise solutions. 15+ years experience, 500+ successful implementations across 15+ countries. Get expert consultation today.",
  keywords = "Oracle ERP implementation, custom desktop applications, enterprise software solutions, Windows desktop development, business automation, ERP consulting, Oracle consultant Egypt, custom software development, enterprise resource planning, business intelligence, supply chain management, healthcare management system, financial management software, inventory management, HR management system",
  image = "https://activesoft.com/og-image.jpg",
  url = "https://activesoft.com/",
  type = "website",
  structuredData
}) => {
  return (
    <Helmet>
      {/* Primary Meta Tags */}
      <title>{title}</title>
      <meta name="title" content={title} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />
      <meta property="og:image:width" content="1200" />
      <meta property="og:image:height" content="630" />
      <meta property="og:site_name" content="Active Soft" />
      
      {/* Twitter */}
      <meta property="twitter:card" content="summary_large_image" />
      <meta property="twitter:url" content={url} />
      <meta property="twitter:title" content={title} />
      <meta property="twitter:description" content={description} />
      <meta property="twitter:image" content={image} />
      
      {/* Canonical URL */}
      <link rel="canonical" href={url} />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
};

export default SEOHead;